const generateMockRecord = (
  dateOffset: number,
  hour: number,
  minute: number,
  road: string,
  isSplashed: boolean
): SprinklerRecord => {
  const now = Date.now();
  const baseDate = new Date(now - dateOffset * 24 * 60 * 60 * 1000);
  baseDate.setHours(hour, minute, 0, 0);
  const timestamp = baseDate.getTime();

  return {
    id: generateId(),
    timestamp,
    date: formatDate(timestamp),
    time: formatTime(timestamp),
    hour,
    minute,
    dayOfWeek: baseDate.getDay(),
    road,
    isSplashed,
    note: undefined,
    createdAt: timestamp,
    updatedAt: timestamp,
    dataSource: 'local',
  };
};

